# ── Tool 1: Wikipedia Search (External API) ────────────────────────────────────
@tool
def wikipedia_search(query: str) -> str:
    """Search Wikipedia for information about any topic. Use this for factual questions about people, places, history, science, etc."""
    try:
        wikipedia.set_lang("en")
        result = wikipedia.summary(query, sentences=4, auto_suggest=True)
        return f"Wikipedia result for '{query}':\n{result}"
    except wikipedia.exceptions.DisambiguationError as e:
        # Try the first suggestion
        try:
            result = wikipedia.summary(e.options[0], sentences=4)
            return f"Wikipedia result for '{e.options[0]}':\n{result}"
        except Exception:
            return f"Multiple results found for '{query}'. Please be more specific. Options: {', '.join(e.options[:5])}"
    except wikipedia.exceptions.PageError:
        return f"No Wikipedia page found for '{query}'. Try a different search term."
    except Exception as e:
        return f"Wikipedia search error: {str(e)}"


# ── Tool 2: Weather (External API — wttr.in, completely free, no key needed) ───
@tool
def get_weather(city: str) -> str:
    """Get current weather for any city. Use this when the user asks about weather conditions."""
    try:
        url = f"https://wttr.in/{city}?format=j1"
        headers = {"User-Agent": "Mozilla/5.0"}
        response = requests.get(url, headers=headers, timeout=10)
        data = response.json()

        current = data["current_condition"][0]
        area = data["nearest_area"][0]
        area_name = area["areaName"][0]["value"]
        country = area["country"][0]["value"]

        temp_c = current["temp_C"]
        feels_like = current["FeelsLikeC"]
        humidity = current["humidity"]
        desc = current["weatherDesc"][0]["value"]
        wind_kmph = current["windspeedKmph"]
        visibility = current["visibility"]

        return (
            f"🌤 Weather in {area_name}, {country}:\n"
            f"  Condition: {desc}\n"
            f"  Temperature: {temp_c}°C (Feels like {feels_like}°C)\n"
            f"  Humidity: {humidity}%\n"
            f"  Wind Speed: {wind_kmph} km/h\n"
            f"  Visibility: {visibility} km"
        )
    except Exception as e:
        return f"Could not retrieve weather for '{city}': {str(e)}"


# ── Tool 3: Joke API (External API — Official Joke API, free, no key) ──────────
@tool
def get_joke(category: str = "general") -> str:
    """Fetch a random joke. Categories: general, programming, knock-knock. Use when the user wants a joke or some humor."""
    try:
        valid_categories = ["general", "programming", "knock-knock"]
        cat = category.lower() if category.lower() in valid_categories else "general"
        url = f"https://official-joke-api.appspot.com/jokes/{cat}/random"
        response = requests.get(url, timeout=10)
        jokes = response.json()
        if isinstance(jokes, list):
            joke = jokes[0]
        else:
            joke = jokes
        return f"😄 {joke['setup']}\n\n👉 {joke['punchline']}"
    except Exception as e:
        return f"Could not fetch a joke: {str(e)}"


# ── Tool 4: Calculator (Custom Python Tool) ────────────────────────────────────
@tool
def calculator(expression: str) -> str:
    """
    Evaluate mathematical expressions safely. Supports: +, -, *, /, **, sqrt(), sin(), cos(), tan(), log(), pi, e.
    Examples: '2 + 2', 'sqrt(144)', '3**4', 'log(100)', 'pi * 5**2'
    """
    try:
        # Safe namespace with math functions
        safe_namespace = {
            "__builtins__": {},
            "sqrt": math.sqrt,
            "sin": math.sin,
            "cos": math.cos,
            "tan": math.tan,
            "log": math.log10,
            "ln": math.log,
            "abs": abs,
            "round": round,
            "pi": math.pi,
            "e": math.e,
            "pow": pow,
            "floor": math.floor,
            "ceil": math.ceil,
        }
        result = eval(expression, safe_namespace)
        return f"🧮 {expression} = {result}"
    except ZeroDivisionError:
        return "Error: Division by zero is undefined."
    except Exception as e:
        return f"Calculator error: {str(e)}. Make sure to use valid math syntax."


