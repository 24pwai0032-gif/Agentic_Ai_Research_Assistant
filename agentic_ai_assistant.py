"""## Gradio Interface"""

import uuid

# Each Gradio session gets a unique thread ID for isolated memory
def create_gradio_interface():

    def respond(message, history, session_id):
        """Process user message and return AI response."""
        if not session_id:
            session_id = str(uuid.uuid4())

        try:
            config = {"configurable": {"thread_id": session_id}}
            result = graph.invoke(
                {"messages": [HumanMessage(content=message)]},
                config=config
            )
            ai_messages = [m for m in result["messages"] if isinstance(m, AIMessage)]
            response = ai_messages[-1].content if ai_messages else "I couldn't generate a response."
        except Exception as e:
            response = f"❌ Error: {str(e)}\n\nPlease check your GROQ_API_KEY."

        return response

    def new_session():
        """Generate a new session ID to reset memory."""
        return str(uuid.uuid4()), []

    # ── Gradio UI ──────────────────────────────────────────────────────────────
    with gr.Blocks(
        title="🤖 Agentic AI Assistant",
        theme=gr.themes.Soft(primary_hue="blue", secondary_hue="slate"),
        css="""
            .header { text-align: center; padding: 20px; }
            .tool-badge {
                display: inline-block;
                background: #e8f4fd;
                border: 1px solid #bee3f8;
                border-radius: 12px;
                padding: 4px 12px;
                margin: 4px;
                font-size: 13px;
                color: #2b6cb0;
            }
        """
    ) as demo:

        # ── Header ─────────────────────────────────────────────────────────────
        gr.HTML("""
        <div class="header">
            <h1>🤖 Agentic AI Research Assistant</h1>
            <p style="color: #666; font-size: 15px;">Powered by LangChain + LangGraph + Groq (LLaMA 3)</p>
            <div>
                <span class="tool-badge">📚 Wikipedia Search</span>
                <span class="tool-badge">🌤 Live Weather</span>
                <span class="tool-badge">🧮 Calculator</span>
                <span class="tool-badge">😄 Joke Generator</span>
            </div>
        </div>
        """)

        # ── State ──────────────────────────────────────────────────────────────
        session_id = gr.State(value=str(uuid.uuid4()))

        # ── Chat Interface ─────────────────────────────────────────────────────
        with gr.Row():
            with gr.Column(scale=3):
                chatbot = gr.Chatbot(
                    label="Conversation",
                    height=480,
                    show_label=True,
                    avatar_images=(None, "https://api.dicebear.com/7.x/bottts/svg?seed=agent"),
                    bubble_full_width=False,
                )
                with gr.Row():
                    msg_input = gr.Textbox(
                        placeholder="Ask me anything... (weather, facts, math, jokes)",
                        label="Your Message",
                        scale=4,
                        lines=1,
                    )
                    send_btn = gr.Button("Send 🚀", variant="primary", scale=1)

            with gr.Column(scale=1):
                gr.Markdown("### 🎯 Try these prompts")

                example_prompts = [
                    "What's the weather in Tokyo?",
                    "Tell me about Albert Einstein",
                    "Calculate sqrt(1764) + 15*8",
                    "Tell me a programming joke",
                    "What is the capital of Australia?",
                    "What is 2^10 divided by 8?",
                    "Tell me about black holes",
                    "Weather in Karachi",
                ]

                for prompt in example_prompts:
                    gr.Button(prompt, size="sm").click(
                        fn=lambda p=prompt: p,
                        outputs=msg_input
                    )

                gr.Markdown("---")
                new_chat_btn = gr.Button("🔄 New Conversation", variant="secondary")
                gr.Markdown(
                    "*Each session has its own memory. Click 'New Conversation' to start fresh.*",
                    elem_classes=["hint"]
                )

        # ── Info Accordion ─────────────────────────────────────────────────────
        with gr.Accordion("📖 About this Application", open=False):
            gr.Markdown("""
            ## Architecture
            This app uses a **LangGraph ReAct-style agentic workflow**:

            ```
            User Input → [Agent Node] → decides → [Tool Node] → back to [Agent Node] → Response
            ```

            - **LangGraph** manages the stateful workflow with conditional routing
            - **MemorySaver** checkpoints keep conversation history per session
            - **Groq** provides fast, free inference with LLaMA 3
            - **Tools** are called automatically based on user intent

            ## Memory
            - Each conversation has a unique `thread_id`
            - LangGraph's `MemorySaver` persists message history across turns
            - Click **New Conversation** to start a fresh session
            """)

        # ── Event Handlers ─────────────────────────────────────────────────────
        def user_submit(message, history, sid):
            if not message.strip():
                return history, "", sid
            ai_response = respond(message, history, sid)
            history = history + [[message, ai_response]]
            return history, "", sid

        send_btn.click(
            fn=user_submit,
            inputs=[msg_input, chatbot, session_id],
            outputs=[chatbot, msg_input, session_id]
        )
        msg_input.submit(
            fn=user_submit,
            inputs=[msg_input, chatbot, session_id],
            outputs=[chatbot, msg_input, session_id]
        )
        new_chat_btn.click(
            fn=new_session,
            outputs=[session_id, chatbot]
        )

    return demo


print("✅ Gradio interface defined!")

