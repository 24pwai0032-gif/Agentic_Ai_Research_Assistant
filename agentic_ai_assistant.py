"""## Defining LangGraph State & Workflow

### Workflow Architecture
```
User Input
    │
    ▼
[agent_node]  ← LLM decides: respond directly OR call a tool
    │
    ├── tool_calls present? ──YES──► [tool_node] ──► back to [agent_node]
    │
    └── No tools needed? ─────────► END (final response)
```
"""

# ── Graph State ────────────────────────────────────────────────────────────────
class AgentState(TypedDict):
    """State holds the full conversation message history."""
    messages: Annotated[list, add_messages]


# ── System Prompt ──────────────────────────────────────────────────────────────
SYSTEM_PROMPT = """You are a helpful, friendly AI assistant with access to the following tools:

1. wikipedia_search — Search Wikipedia for factual information on any topic
2. get_weather — Get live weather for any city
3. get_joke — Fetch a random joke (categories: general, programming, knock-knock)
4. calculator — Evaluate math expressions safely

Guidelines:
- Always use tools when the user asks for information you can look up
- For weather questions, extract the city name and call get_weather
- For math questions, always use the calculator tool
- For factual questions, use wikipedia_search
- Be concise, friendly, and helpful
- Always explain what tool you used and summarize results clearly
"""


# ── Agent Node ─────────────────────────────────────────────────────────────────
def agent_node(state: AgentState) -> AgentState:
    """Main agent node: calls LLM with tools."""
    messages = state["messages"]

    # Prepend system prompt if not already there
    if not messages or not isinstance(messages[0], SystemMessage):
        messages = [SystemMessage(content=SYSTEM_PROMPT)] + list(messages)

    response = llm_with_tools.invoke(messages)
    return {"messages": [response]}


# ── Conditional Routing ────────────────────────────────────────────────────────
def should_use_tools(state: AgentState) -> str:
    """Route to tools if the LLM requested tool calls, otherwise end."""
    last_message = state["messages"][-1]
    if hasattr(last_message, "tool_calls") and last_message.tool_calls:
        return "use_tools"
    return "end"


# ── Build Graph ────────────────────────────────────────────────────────────────
tool_node = ToolNode(tools)  # Pre-built node that executes tools
memory = MemorySaver()        # In-memory checkpointer for conversation history

graph_builder = StateGraph(AgentState)

# Add nodes
graph_builder.add_node("agent", agent_node)
graph_builder.add_node("tools", tool_node)

# Set entry point
graph_builder.set_entry_point("agent")

# Conditional edge: after agent, decide to use tools or end
graph_builder.add_conditional_edges(
    "agent",
    should_use_tools,
    {"use_tools": "tools", "end": END}
)

# After tools execute, return to agent for final response
graph_builder.add_edge("tools", "agent")

# Compile with memory checkpointing
graph = graph_builder.compile(checkpointer=memory)

print("✅ LangGraph workflow compiled with memory checkpointing!")

